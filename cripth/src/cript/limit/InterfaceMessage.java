    private void writeMsgKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_writeMsgKeyPressed
        if(evt.getKeyCode()== evt.VK_ENTER && writeMsg.getText().length()>0){
            evt.consume();//Cancela o processo do evento keyevent caso seja o Enter digitado.
            btnSend.doClick();            
        }
    }//GEN-LAST:event_writeMsgKeyPressed

